# допиши введення оцінок через пробіл
marks = input('Введіть оцінки через пробіл:')
marks = marks.split(' ')

print('Аналіз набору', marks)
growth = 1 # за умовчанням у всіх зростання
for i in range (len (marks) - 1):
    if marks [i + 1] < marks [i]: # якщо поточна оцінка менше наступної
        growth = -1 # успішність не стабільна
if growth == 1:
    print('Стабільна успішність!')
else:
    print('Стабільної успішності немає')
